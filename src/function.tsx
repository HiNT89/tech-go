const filterProductCart = (
  cart: {
    id: string;
    productID: string;
    count: number;
    color: string;
  }[],
  listProduct: {
    productName: string;
    nsx: string;
    price: number;
    sale: number;
    percentSale: number;
    description: string;
    productID: string;
    type: string;
    count: {
      id: string;
      color: string;
      count: number;
      remaining: number;
      imgURL: string;
      codeColor: string;
    }[];
    id: string;
  }[]
) => {
  if (cart.length) {
    const newListProductCart = cart.map((item) => {
      const product = listProduct.filter((it) => it.id === item.productID)[0];
      const { productName, sale, price, count } = product;
      const imgURL = count.filter((it) => it.color === item.color)[0].imgURL;
      return {
        ...item,
        productName: productName,
        price: sale ? sale : price,
        imgURL: imgURL,
      };
    });
    return newListProductCart;
  } else {
    return [];
  }
};
